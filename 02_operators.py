### Comparative Operators ### 
print("Comparative Operators")
print (3 > 4)
print (3 < 4)
print (3 >= 4)
print (3 <= 4)
print (3 == 4)
print (3 != 4)
print (3 > 4 == 2)
print()


print("Hello" > "Python")
print("Hello" < "Python")
print("aaaa" >= "abaa") # Ordenación alfabética por ASCII
print(len("aaaa") >= len("abaa")) # Cuenta caractereres
print("Hello" <= "Python")
print("Hello" == "Python")
print("Hello" != "Python")


### Logics Operators ### 
print (3 > 4 and "Hello" > "Python")
print (3 > 4 or "Hello" > "Python")
print (not(3 > 4))
