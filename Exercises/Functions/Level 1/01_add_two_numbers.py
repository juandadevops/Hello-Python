
def add_two_numbers (num1, num2):
    """ Function that takes two parameters and it returns a sum

    Args:
        num1 (float): First number to sum
        num2 (float): Seconds number to sum

    Returns:
        _type_: The sum
    """
    return num1 + num2

num1= 50.1
num2= 50.1
print(f"The sum of {num1} + {num2} is equal to {add_two_numbers(num1, num2)}")